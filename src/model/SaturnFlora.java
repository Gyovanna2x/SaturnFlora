package model;

public class SaturnFlora {

	private int id;
	
	private String personalizado;
	private String dataEntrega;
	private String MsgPersonalizada;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getPersonalizado() {
		return personalizado;
	}
	public void setPersonalizado(String personalizado) {
		this.personalizado = personalizado;
	}
	public String getDataEntrega() {
		return dataEntrega;
	}
	public void setDataEntrega(String dataEntrega) {
		this.dataEntrega = dataEntrega;
	}
	public String getMsgPersonalizada() {
		return MsgPersonalizada;
	}
	public void setMsgPersonalizada(String msgPersonalizada) {
		MsgPersonalizada = msgPersonalizada;
	}
	
	
}
